'''Дані про температуру повітря і кількості опадів за декаду квітня
зберігаються в масивах. Визначити кількість опадів, що випали у вигляді дощу і у
вигляді снігу за цю декаду.
Виконал Пахомов Олександр'''
import numpy as np
a=np.array([12,23,12,31,11,15,4,11,18,8])
t=np.array([-1,4,2,1,-2,-4,0,7,5,4])
snow=0
rain=0
for i in range(10):
    if t[i]>0:
        rain+=a[i]
    else:
        snow+=a[i]
print(f'rain:{rain}')
print(f'snow:{snow}')
